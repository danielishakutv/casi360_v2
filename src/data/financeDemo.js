export function getFinanceOverviewStats() {
  const totalAllocated = demoFinanceBudgetLines.reduce((sum, line) => sum + line.allocated_amount, 0)
  const totalCommitted = demoFinanceBudgetLines.reduce((sum, line) => sum + line.committed_amount, 0)
  const totalSpent = demoFinanceBudgetLines.reduce((sum, line) => sum + line.actual_spent_amount, 0)
  const totalPending = demoFinanceBudgetLines.reduce((sum, line) => sum + line.pending_request_amount, 0)
  const totalAvailable = demoFinanceBudgetLines.reduce((sum, line) => sum + line.available_amount, 0)
  const pendingApprovals = demoFinanceApprovals.filter((item) => item.status === 'pending')
  const flaggedLines = demoFinanceBudgetLines.filter((line) => ['low', 'critical', 'overdrawn'].includes(line.status)).length

  return {
    totalAllocated,
    totalCommitted,
    totalSpent,
    totalPending,
    totalAvailable,
    totalBudgetLines: demoFinanceBudgetLines.length,
    totalProjects: new Set(demoFinanceBudgetLines.map((line) => line.project_id)).size,
    pendingApprovals: pendingApprovals.length,
    pendingApprovalAmount: pendingApprovals.reduce((sum, item) => sum + item.amount_requested, 0),
    flaggedLines,
  }
}

export function getFinanceFilterOptions() {
  return {
    projects: [...new Set(demoFinanceBudgetLines.map((line) => line.project_name))].sort(),
    departments: [...new Set(demoFinanceBudgetLines.map((line) => line.department))].sort(),
    categories: [...new Set(demoFinanceBudgetLines.map((line) => line.category))].sort(),
    statuses: ['healthy', 'low', 'critical', 'overdrawn'],
  }
}